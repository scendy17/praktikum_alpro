#Menginputkan bilangan

a = int(input('Masukkan bilangan pertama: '))
b = int(input('Masukkan bilangan kedua: '))

#Operasi aritmatika
penjumlahan = a + b
pengurangan = a - b
perkalian = a * b
pembagian = a / b
modulus = a % b
pembagian_kebawah = a // b
pangkat = a ** b
precedence = a + b * 2

#Menampilkan hasil
print('Hasil penjumlahan a + b =', penjumlahan)
print('Hasil pengurangan a - b =', pengurangan)
print('Hasil perkalian a * b =', perkalian)
print('Hasil pembagian a / b =', pembagian)
print('Hasil modulus a % b =', modulus)
print('Hasil pembagian kebawah a // b =', pembagian_kebawah)
print('Hasil pangkat a ** b =', pangkat)
print('Hasil precedence a + b * 2 =', precedence)